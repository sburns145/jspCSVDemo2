package org.mypackage.hello;

/**
 *
 * @author scott.burns
 */
public class NameHandler {
    private String name;
    private int x;
    
    public NameHandler() {
        name = null;
        x = 10;
    }

    /**
     * @return the name
     */
    public String getName() {
        return name;
    }

    /**
     * @param name the name to set
     */
    public void setName(String name) {
        this.name = name;
    }

    /**
     * @return the x
     */
    public int getX() {
        return x;
    }

    /**
     * @param x the x to set
     */
    public void setX(int x) {
        this.x = x;
    }
}
